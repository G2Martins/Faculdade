%% Arrendondamento FIX
% Arrendonda em direção ao zero

fix(5.1) % Arrendonda para 5
fix(5.9) % Arrendonda para 5
fix(-5.9) % Arrendonda para -5

%% Arrendondamento FLOOR
% Arrendonda em direção ao MENOS INFINITO

floor(5.1) % Arrendonda para 5
floor(5.9) % Arrendonda para 5
floor(-5.9) % Arrendonda para -6 em direção ao menos infinito

%% Arrendondamento CEIL
% Arrendonda em direção ao MAIS INFINITO

ceil(5.1) % Arrendonda para 6
ceil(5.9) % Arrendonda para 6
ceil(-5.9) % Arrendonda para -5 em direção ao mais infinito

%% Arrendondamento ROUND
% Arrendonda em direção ao inteiro mais próximo

round(5.1) % Arrendonda para 5
round(5.9) % Arrendonda para 6
round(-5.9) % Arrendonda para -6
