%% Conversões

% Conversão de radianos para graus
(0.5 *pi) * (180/pi) % Exibe -> 90°
(1 *pi) * (180/pi) % Exibe -> 180°

rad2deg(0.5*pi) % Meio Pi é 90°
rad2deg(1*pi) % 180°

% Conversão de graus para radianos
(90) * (pi/180) % Exibe o valor de 90° em radianos
(180) * (pi/180) % Exibe o valor de 180° em radianos

deg2rad(45)
deg2rad(90)
deg2rad(180)