%% Mínimo Multíplo Comum
% Retorna o menor valor que seja múltiplo dos dois argumentos de entrada

% Múltiplos de 3 e 4 são: 12, 24, 36, 48...

lcm(3,4) % Exibe -> 12

% Múltiplos de 5 e 4 são: 20, 40, 60, 80...

lcm(5,4) % Exibe -> 20

%% Máximo Divisor Comum
% Retorna o máximo divisor entre os dois argumentos de entrada

% Os divisores comuns de 12 e 18 são: 1, 2, 3 e 6.

gcd(12, 18) % Exibe -> 6